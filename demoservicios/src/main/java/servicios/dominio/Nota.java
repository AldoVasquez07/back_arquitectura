package servicios.dominio;

public class Nota {
	private String curso;
	private double nota;
	
	public Nota() {
	}
	
	public String getCurso() {
		return this.curso;
	}
	public void setCurso(String curso) {
		this.curso = curso;
	}
	public double getNota() {
		return this.nota;
	}
	public void setNota(double nota) {
		this.nota = nota;
	}
	
	
	
}
